#!/usr/bin/env node
/**
 * Multi-window protocol — edit-time scope drift checker.
 *
 * Runs against the WORKING TREE (modified + untracked + staged), not just
 * staged-for-commit files. Catches scope violations as soon as they appear,
 * not at commit time. Closes Sprint 6 backlog P5-1.
 *
 * Usage:
 *   node scripts/window-check-edits.mjs                 # human-readable
 *   node scripts/window-check-edits.mjs --json          # machine-readable
 *   node scripts/window-check-edits.mjs --quiet         # exit code only
 *
 * Exit codes:
 *   0 — all working-tree changes within scope (or no claim → no opinion)
 *   1 — out-of-scope changes detected
 *   2 — error (no current branch, etc)
 *
 * Wiring:
 *   - Run manually after a batch of edits.
 *   - Or wire into a Claude Code PostToolUse hook on Edit/Write to fire
 *     automatically after every file edit (~/.claude/settings.json).
 */

import { execSync } from 'node:child_process'
import { currentBranch, fileMatchesScope, loadClaimForBranch } from './lib/window-claim.mjs'

const args = process.argv.slice(2)
const json = args.includes('--json')
const quiet = args.includes('--quiet')

// Self-claim files are auto-generated by window-{start,end,heartbeat}.mjs, not
// user edits. Pre-commit applies the same exclusion (window-scope-check.mjs).
const SELF_CLAIM_PATTERN = /^\.claude\/active-windows\/.*\.json$/

/** All working-tree changed paths: modified, added (staged + unstaged), and untracked. */
function workingTreeChanges() {
  // -z = NUL-terminated, no quoting; --untracked-files=all = include nested
  const out = execSync('git status --porcelain=v1 -z --untracked-files=all', {
    encoding: 'utf8',
  })
  if (!out) return []
  // porcelain v1 format: "XY <space> <path>\0" (rename: " <path><tab><path>\0")
  // We split by NUL, then for each entry the first 2 chars are status flags,
  // followed by space + path. Renames produce "R  oldpath\0newpath\0" but we
  // just take whatever path follows the status.
  const entries = out.split('\0').filter(Boolean)
  const paths = []
  for (let i = 0; i < entries.length; i++) {
    const entry = entries[i]
    if (entry.length < 4) continue
    const status = entry.slice(0, 2)
    const path = entry.slice(3)
    paths.push(path)
    // Rename / copy: next entry is the old path; skip it.
    if (status[0] === 'R' || status[0] === 'C') i += 1
  }
  return paths
}

let branch
try {
  branch = currentBranch()
} catch (err) {
  console.error(`window-check-edits: ${err instanceof Error ? err.message : String(err)}`)
  process.exit(2)
}

const claim = loadClaimForBranch(branch)
if (!claim) {
  if (json) {
    console.log(JSON.stringify({ branch, has_claim: false, in_scope: [], out_of_scope: [] }))
  } else if (!quiet) {
    console.log(`window-check-edits: no active claim for branch '${branch}' — no opinion`)
  }
  process.exit(0)
}

const changes = workingTreeChanges().filter((f) => !SELF_CLAIM_PATTERN.test(f))
const inScope = []
const outOfScope = []
for (const f of changes) {
  if (fileMatchesScope(f, claim.scope || [])) {
    inScope.push(f)
  } else {
    outOfScope.push(f)
  }
}

if (json) {
  console.log(
    JSON.stringify(
      {
        branch,
        has_claim: true,
        scope: claim.scope || [],
        in_scope: inScope,
        out_of_scope: outOfScope,
      },
      null,
      2
    )
  )
}

if (outOfScope.length === 0) {
  if (!json && !quiet) {
    console.log(
      `✅ All ${changes.length} working-tree change(s) within scope of branch '${branch}'.`
    )
  }
  process.exit(0)
}

if (!json && !quiet) {
  console.error(
    `⚠️  ${outOfScope.length} working-tree change(s) outside the active scope of '${branch}':`
  )
  console.error('')
  for (const f of outOfScope) console.error(`     - ${f}`)
  console.error('')
  console.error(`   Active scope:`)
  for (const g of claim.scope || []) console.error(`     - ${g}`)
  console.error('')
  console.error(`   Either revert those edits, or extend scope:`)
  console.error(`     node scripts/window-end.mjs && node scripts/window-start.mjs --scope ...`)
  console.error('')
  console.error(
    `   This is a heads-up at edit time — committing them would also fail at the pre-commit gate.`
  )
}
process.exit(1)
